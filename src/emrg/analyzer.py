def _freeze_dict(value: dict[str, Any]) -> MappingProxyType[str, Any]:
    """Wrap a dict in a read-only ``MappingProxyType``."""
    return MappingProxyType(value)


@dataclass(frozen=True)
class CircuitFeatures:
    """Immutable snapshot of circuit properties relevant to error mitigation.

    All fields are derived from Qiskit introspection -- no simulation needed.

    Attributes:
        num_qubits: Number of quantum bits in the circuit.
        depth: Circuit depth (longest path of dependent operations).
        gate_counts: Read-only mapping of gate name to count
            (e.g. ``{'cx': 4, 'h': 3}``).
        total_gate_count: Total number of gate operations.
        multi_qubit_gate_count: Number of multi-qubit gates (2-qubit and
            3-qubit, e.g. CX, CCX, CSWAP).
        single_qubit_gate_count: Number of single-qubit gates.
        num_parameters: Number of unbound parameters (0 for non-variational).
        has_measurements: Whether the circuit contains measurement operations.
        estimated_noise_factor: Simple heuristic noise proxy
            (``multi_qubit_gate_count * multi_qubit_error_rate
              + single_qubit_gate_count * single_qubit_error_rate``).
        noise_category: Human-readable category: ``"low"``, ``"moderate"``,
            or ``"high"``.
        noise_model_available: Whether a noise model is available for PEC.
            Defaults to ``False``; set to ``True`` when a depolarizing (or
            other) noise model can be supplied so the heuristic engine may
            consider Probabilistic Error Cancellation.
        pec_overhead_estimate: Approximate PEC sampling overhead computed as
            ``exp(2 * noise_factor * multi_qubit_gate_count)``.  Values much
            larger than ~1000 make PEC impractical.
        layer_heterogeneity: Ratio of the maximum to minimum multi-qubit gate
            count across circuit layers (with +1 denominator smoothing).
            Higher values indicate more varied layer structure, which favours
            layerwise noise scaling over uniform folding.  Returns 0.0 when
            the circuit has fewer than two layers containing multi-qubit gates.
        non_clifford_count: Number of non-Clifford gates in the circuit.
            Non-Clifford gates include T, Tdg, and any rotation gate whose
            angle is not a multiple of pi/2.
        non_clifford_fraction: Fraction of gates that are non-Clifford
            (``non_clifford_count / total_gate_count``).  0.0 means all
            Clifford, 1.0 means all non-Clifford.
    """

    num_qubits: int
    depth: int
    gate_counts: Mapping[str, int] = field(default_factory=dict)
    total_gate_count: int = 0
    multi_qubit_gate_count: int = 0
    single_qubit_gate_count: int = 0
    num_parameters: int = 0
    has_measurements: bool = False
    estimated_noise_factor: float = 0.0
    noise_category: str = "low"
    noise_model_available: bool = False
    pec_overhead_estimate: float = 1.0
    layer_heterogeneity: float = 0.0
    non_clifford_count: int = 0
    non_clifford_fraction: float = 0.0


