def _validate_qiskit_circuit(qc: QuantumCircuit) -> list[str]:
    """Validate the circuit and return a list of warning messages (may be empty).

    Checks:
    - Circuit must be a ``QuantumCircuit`` instance.
    - Circuit must contain at least one gate operation.
    - Circuit should have measurements for meaningful mitigation.

    Raises:
        TypeError: If *qc* is not a ``QuantumCircuit``.
        ValueError: If the circuit has zero gate operations.
    """
    from qiskit import QuantumCircuit as _QuantumCircuit  # lazy runtime import

    if not isinstance(qc, _QuantumCircuit):
        raise TypeError(f"Expected a qiskit.QuantumCircuit, got {type(qc).__name__}.")

    ops = qc.count_ops()
    # Filter out measurement/barrier/delay -- only count real gates
    gate_ops = {
        name: count
        for name, count in ops.items()
        if name not in ("measure", "barrier", "delay", "reset")
    }

    if not gate_ops:
        raise ValueError(
            "Circuit has no gate operations. "
            "Provide a circuit with at least one quantum gate."
        )

    warns: list[str] = []
    if "measure" not in ops:
        warns.append(
            "Circuit has no measurements. Error mitigation requires "
            "measurement results -- consider adding qc.measure_all()."
        )
    if qc.num_parameters > 0:
        warns.append(
            f"Circuit has {qc.num_parameters} unbound parameter(s). "
            "Parameters must be bound before execution "
            "(e.g. qc = qc.assign_parameters({...})). "
            "EMRG will analyze the circuit structure, but the generated "
            "code cannot run until all parameters are resolved."
        )
    return warns


# ---------------------------------------------------------------------------
# Noise estimation
# ---------------------------------------------------------------------------


def _classify_noise(noise_factor: float) -> str:
    """Map a numeric noise factor to a human-readable category.

    Thresholds are deliberately conservative and tunable.
    """
    if noise_factor < NOISE_THRESHOLD_LOW:
        return "low"
    elif noise_factor < NOISE_THRESHOLD_MODERATE:
        return "moderate"
    else:
        return "high"


def _estimate_noise(
    multi_qubit_count: int,
    single_qubit_count: int,
    *,
    multi_qubit_error_rate: float = DEFAULT_MULTI_QUBIT_ERROR_RATE,
    single_qubit_error_rate: float = DEFAULT_SINGLE_QUBIT_ERROR_RATE,
) -> float:
    """Return a simple additive noise proxy.

    This is *not* a rigorous noise model -- it is a fast heuristic used to
    guide mitigation strategy selection.
    """
    return (
        multi_qubit_count * multi_qubit_error_rate
        + single_qubit_count * single_qubit_error_rate
    )


def _estimate_pec_overhead(
    noise_factor: float,
    multi_qubit_gate_count: int,
) -> float:
    """Return the approximate PEC sampling overhead.

    The formula ``exp(2 * noise_factor * multi_qubit_gate_count)`` captures
    the exponential cost that makes PEC impractical for large or very noisy
    circuits.  The exponent is capped at 700 to avoid ``OverflowError`` on
    circuits with hundreds of multi-qubit gates.
    """
    exponent = PEC_OVERHEAD_NOISE_MULTIPLIER * noise_factor * multi_qubit_gate_count
    # math.exp overflows around ~709; cap to prevent OverflowError.
    if exponent > 700:
        return math.inf
    return math.exp(exponent)


# ---------------------------------------------------------------------------
# DAG-derived features
# ---------------------------------------------------------------------------


def _is_clifford_angle(angle: float) -> bool:
    """Return ``True`` if *angle* is a multiple of pi/2 (within tolerance).

    Clifford rotations are those at multiples of pi/2:
    0, pi/2, pi, 3*pi/2, 2*pi, etc.
    """
    units = angle / (math.pi / 2)
    return abs(units - round(units)) < 1e-8


_NON_GATE_OP_NAMES: frozenset[str] = frozenset({"measure", "barrier", "delay", "reset"})


def _walk_qiskit_dag(qc: QuantumCircuit) -> tuple[float, int]:
    """Single DAG pass returning ``(layer_heterogeneity, non_clifford_count)``.

    Building the DAG dominates ``analyze_circuit`` for large circuits, so
    both per-layer statistics and non-Clifford counting share one pass.

    *layer_heterogeneity* is ``max(counts) / (min(counts) + 1)`` over
    circuit layers that contain at least one multi-qubit gate; 0.0 when
    fewer than two such layers exist.

    *non_clifford_count* classifies each op:
    - Gates in ``CLIFFORD_GATE_NAMES`` are always Clifford.
    - ``t``/``tdg`` are always non-Clifford.
    - Rotation gates are Clifford only when all parameters are multiples
      of pi/2.
    - Unknown gates are conservatively counted as non-Clifford.
    """
    from qiskit.converters import circuit_to_dag

    dag = circuit_to_dag(qc)

    mq_counts: list[int] = []
    non_clifford = 0

    for layer_dict in dag.layers():
        layer_dag = layer_dict["graph"]
        mq_in_layer = 0

        for node in layer_dag.op_nodes():
            name = node.op.name
            if name in _NON_GATE_OP_NAMES:
                continue

            if name in MULTI_QUBIT_GATE_NAMES:
                mq_in_layer += 1

            # Clifford classification.
            if name in CLIFFORD_GATE_NAMES:
                continue
            if name in ("t", "tdg"):
                non_clifford += 1
                continue
            if name in _ROTATION_GATE_NAMES:
                params = node.op.params
                if params and all(
                    isinstance(p, (int, float)) and _is_clifford_angle(float(p))
                    for p in params
                ):
                    continue
                non_clifford += 1
                continue
            # Unknown gate -- conservatively non-Clifford.
            non_clifford += 1

        if mq_in_layer > 0:
            mq_counts.append(mq_in_layer)

    if len(mq_counts) < 2:
        layer_het = 0.0
    else:
        layer_het = max(mq_counts) / (min(mq_counts) + 1)

    return layer_het, non_clifford


